<p class="Par1">Strathmore Archery Club is a non-profit organization dedicated to promoting the sport of archery in Strathmore University.<br>We offer a variety of programs and activities for archers of all skill levels on campus.</p>
    <p class="Par1">Target archery is a sport in which archers shoot at stationary circular targets from set distances.<br>It is the most popular form of archery, and is the type that most beginners or "Have a go" archery session participants try first.</p>
        <p class="Par1">The target used in target archery is a round target with 10 scoring rings.<br>The inner ring scores 10 points, and the outer ring scores 1 point. The other rings score in between.<br>The target is usually made of paper or cloth, and is mounted on a backboard.</p>  

